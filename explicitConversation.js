console.log("---- Convert number string and boolean values to numbers, In that case we can use Number() ----")

//string to number
var result = Number('324');
console.log(result);

var result = Number('324e-1')
console.log(result);

//Boolean to number
 var result = Number(true);
 console.log(result);  

 var result = Number(false);
 console.log(result);


